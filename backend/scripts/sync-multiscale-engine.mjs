import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const backendRoot = path.resolve(__dirname, '..');
const projectRoot = path.resolve(backendRoot, '..');
const webappRoot = path.resolve(projectRoot, 'frontend/webapp');
const multiscaleSourcePath = path.resolve(webappRoot, 'src/lib/multiscaleLab.ts');
const generatedDir = path.resolve(backendRoot, 'lib/generated');
const generatedOutputPath = path.resolve(generatedDir, 'multiscale-engine.mjs');

async function loadTypeScriptRuntime() {
  try {
    const typescriptModule = await import(pathToFileURL(path.resolve(webappRoot, 'node_modules/typescript/lib/typescript.js')).href);
    return typescriptModule.default ?? typescriptModule;
  } catch (error) {
    throw new Error('Generating the backend multiscale engine requires frontend/webapp dependencies. Run `cd frontend/webapp && npm install` first.', { cause: error });
  }
}

export async function syncGeneratedMultiscaleEngine() {
  const ts = await loadTypeScriptRuntime();
  const source = await fs.readFile(multiscaleSourcePath, 'utf8');
  const { outputText } = ts.transpileModule(source, {
    compilerOptions: {
      module: ts.ModuleKind.ES2020,
      target: ts.ScriptTarget.ES2020,
    },
  });

  await fs.mkdir(generatedDir, { recursive: true });
  const output = [
    '// Generated by backend/scripts/sync-multiscale-engine.mjs.',
    '// Source: frontend/webapp/src/lib/multiscaleLab.ts',
    '',
    outputText.trim(),
    '',
  ].join('\n');
  await fs.writeFile(generatedOutputPath, output);

  return generatedOutputPath;
}

const invokedAsEntrypoint = process.argv[1] && path.resolve(process.argv[1]) === __filename;

if (invokedAsEntrypoint) {
  syncGeneratedMultiscaleEngine()
    .then((outputPath) => {
      console.log(`generated backend multiscale engine: ${outputPath}`);
    })
    .catch((error) => {
      console.error(error);
      process.exitCode = 1;
    });
}
